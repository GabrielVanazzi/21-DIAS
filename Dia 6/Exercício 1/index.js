//CRIANDO VARIAVEIS
let valor = parseInt(prompt("Digite um valor inteiro e positivo:"))

// #1 CRIANDO LOOP FOR
for(let i = 0; i <= valor; i++){
    console.log(i)
}

// #2 CRIANDO LOOP FOR
for(let i = 0; i <= valor; i += 5){
    console.log(i)
}

// #3 CRIANDO LOOP FOR
for(let i = valor; i >= 0; i -= 5){
    console.log(i)
}